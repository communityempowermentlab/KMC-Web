function themeBackgroundURL(input) {
    if (input.files && input.files[0]) {
        var reader = new FileReader();
        reader.onload = function(ee) {
            $('.coverimagec1')
                .attr('src', ee.target.result);
        };
        reader.readAsDataURL(input.files[0]);
    }
}
